package edunova;

import java.util.Date;

public class Racun {
	private Date datum;
	private double iznos;
	private int kupac;
	public Date getDatum() {
		return datum;
	}
	public void setDatum(Date datum) {
		this.datum = datum;
	}
	public double getIznos() {
		return iznos;
	}
	public void setIznos(double iznos) {
		this.iznos = iznos;
	}
	public int getKupac() {
		return kupac;
	}
	public void setKupac(int kupac) {
		this.kupac = kupac;
	}

}
